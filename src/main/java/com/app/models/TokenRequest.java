package com.app.models;

// This class represents a request object for handling token-related operations
public class TokenRequest {
    // Field to store the token
    private String token;

    // Public getter method to retrieve the token
    public String getToken() {
        return token;
    }

    // Public setter method to set the token
    public void setToken(String token) {
        this.token = token;
    }
}